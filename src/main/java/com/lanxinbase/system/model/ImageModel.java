package com.lanxinbase.system.model;

public class ImageModel extends Model {
    private int width;
    private int height;

    public ImageModel(){

    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
